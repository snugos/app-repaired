// js/CCStepSequencer.js - MIDI CC Step Sequencer
// Step sequencer for MIDI continuous controllers (CC) - knobs, faders, wheels
import { getMIDICCList } from './MIDICCTypes.js';

let localAppServices = {};
let ccSequencerWindow = null;
let currentCCChannel = 1; // MIDI channel 1-16
let currentCCNumber = 1;  // CC number 0-127
let selectedCell = null;

// Grid settings
const CC_STEPS = 16;      // 16 steps
const CC_ROWS_VISIBLE = 16; // Rows visible at once
const CELL_WIDTH = 40;     // Width of each cell
const CELL_HEIGHT = 24;   // Height of each cell
const HEADER_HEIGHT = 30;
const CHANNEL_COLUMN_WIDTH = 50;
const CC_COLUMN_WIDTH = 60;

// CC values per step: { cc: number, value: number (0-127), curve: 'linear'|'exponential' }
let ccPatternData = Array.from({ length: CC_STEPS }, () => ({ cc: 1, value: 0, curve: 'linear' }));

export function initCCStepSequencer(appServicesFromMain) {
    localAppServices = appServicesFromMain || {};
    console.log('[CCStepSequencer] Module initialized');
}

export function openCCStepSequencer(channel = 1, ccNumber = 1) {
    const windowId = 'ccStepSequencer';
    const openWindows = localAppServices.getOpenWindows ? localAppServices.getOpenWindows() : new Map();

    if (openWindows.has(windowId)) {
        const win = openWindows.get(windowId);
        win.restore();
        renderCCSequencerContent();
        return win;
    }

    const contentContainer = document.createElement('div');
    contentContainer.id = 'ccSequencerContent';
    contentContainer.className = 'p-2 h-full flex flex-col bg-gray-100 dark:bg-slate-800 overflow-hidden';

    const options = {
        width: 820,
        height: 520,
        minWidth: 600,
        minHeight: 350,
        initialContentKey: windowId,
        closable: true,
        minimizable: true,
        resizable: true
    };

    const win = localAppServices.createWindow(windowId, 'CC Step Sequencer', contentContainer, options);
    if (win?.element) {
        ccSequencerWindow = win;
        renderCCSequencerContent();
    }

    return win;
}

